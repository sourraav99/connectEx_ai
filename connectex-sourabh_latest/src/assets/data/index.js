export const dynamicShortsHeight = (insets) => {
  const { height: screenHeight } = Dimensions.get("screen"); // Total screen height
  const { height: windowHeight } = Dimensions.get("window"); // Usable screen height
  // const insets = useSafeAreaInsets(); // Get insets for gesture navigation
  if (!insets || insets.bottom === undefined) {
    // Safe check in case insets is undefined or not passed correctly
    return Dimensions.get("screen").height; // Return default screen height as fallback
  }
  if (Platform.OS === "ios") {
    return screenHeight; // For iOS, return full height
  } else {
    const navigationBarHeight = screenHeight - windowHeight - insets.bottom; // Calculate navigation bar height

    if (navigationBarHeight > 0) {
      // Navigation bar is present
      return screenHeight; // Adjust for navigation bar
    } else {
      // Gesture navigation is being used
      return screenHeight * 0.91; // Adjust for gesture mode
    }
  }
};



